//4. IMPRIMIR LA SUMA ACUMULADA DE LOS PRIMEROS 10 NUMEROS
DECLARE
LV_SUMA NUMBER := 0;
BEGIN
    DBMS_OUTPUT.PUT_LINE('<<<<<<<<<<<<<<SUMA DE LOS PRIMEROS 10 NUMEROS>>>>>>>>>>>>>>');
