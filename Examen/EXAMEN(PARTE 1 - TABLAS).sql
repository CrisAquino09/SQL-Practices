//1.- TABLA EMPLEADO : ID_EMPLEADO, NOMBRE ,    DEPARTAMENTO_ID , SALARIO FECHA_INGRESO
CREATE TABLE EMPLEADO(
    ID_EMPLEADO NUMBER,
    NOMBRE NVARCHAR2(100),
    DEPARTAMENTO_ID NUMBER,
    SALARIO NUMBER,
    FECHA_INGRESO DATE,
    CONSTRAINT EMPLEDO_PK PRIMARY KEY (ID_EMPLEADO),
    CONSTRAINT DEPARTAMENTO_EMPLEADO_FK FOREIGN KEY (DEPARTAMENTO_ID) REFERENCES DEPARTAMENTO
);

//2.- TABLA DEPARTAMENTO :  ID_DEPARTAMENTO, NOMBRE 
CREATE TABLE DEPARTAMENTO(
    ID_DEPARTAMENTO NUMBER,
    NOMBRE NVARCHAR2(100),
    CONSTRAINT DEPARTAMENTO_PK PRIMARY KEY (ID_DEPARTAMENTO)
);

//3.- TABLA PROYECTO :  ID_PROYECTO,  NOMBRE, FECHA_INICIO, FECHA_FIN
CREATE TABLE PROYECTO(
    ID_PROYECTO NUMBER,
    NOMBRE NVARCHAR2(100),
    FECHA_INICIO DATE,
    FECHA_FIN DATE,
    CONSTRAINT PROYECTO_PK PRIMARY KEY (ID_PROYECTO)
);

//4.- TABLA ASIGNACION :  ID_ASIGNACION , EMPLEADO_ID,  PROYECTO_ID, HORAS_TRABAJADAS,
CREATE TABLE ASIGNACION(
    ID_ASIGNACION NUMBER,
    EMPLEADO_ID NUMBER,
    PROYECTO_ID NUMBER,
    HORAS_TRABAJADAS NUMBER,
    CONSTRAINT ASIGNACION_PK PRIMARY KEY (ID_ASIGNACION),
    CONSTRAINT EMPLEADO_ASIGNACION_FK FOREIGN KEY (EMPLEADO_ID) REFERENCES EMPLEADO,
    CONSTRAINT PROYECTO_ASIGNACION_FK FOREIGN KEY (PROYECTO_ID) REFERENCES PROYECTO
);

